# Example foods list
foods = ["Pizza", "Burger", "Pasta", "Sushi", "Tacos"]

# First for loop: Print each food item on a new line
print("Food list (First loop):")
for food in foods:
    print(food)

# Second for loop: Print each food item in a formatted way
print("\nFood list (Second loop):")
for food in foods:
    print(f"- {food}")
