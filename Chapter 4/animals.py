# Animals: List of animals with a common characteristic and print statements about each.

# List of animals that would make great pets
animals = ["Dog", "Cat", "Rabbit"]

# Loop through the list of animals and print a statement about each one
for animal in animals:
    print(f"A {animal} would make a great pet.")

# Print a final statement about what these animals have in common
print("\nAny of these animals would make a great pet!")
