# Pizzas: Think of at least three kinds of your favorite pizza and print a message for each.

# List of favorite pizzas
pizzas = ["Pepperoni", "Margherita", "BBQ Chicken"]

# Loop through the list of pizzas and print a message about each one
for pizza in pizzas:
    print(f"I like {pizza} pizza.")

# Print a final message outside the loop
print("\nI really love pizza!")