# Function definition
def city_country(city, country):
    return f"{city}, {country}"

# Calling the function with three city-country pairs and printing the results
city_info1 = city_country("Santiago", "Chile")
city_info2 = city_country("Tokyo", "Japan")
city_info3 = city_country("Paris", "France")

print(city_info1)
print(city_info2)
print(city_info3)
