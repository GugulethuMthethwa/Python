name = "\t\n  Kefilwe Mthethwa \n\t"

# Print the name with the whitespace characters
print(f"Original name with whitespace: '{name}'")

# Print the name after using lstrip(), rstrip(), and strip()
print(f"Name after lstrip(): '{name.lstrip()}'")
print(f"Name after rstrip(): '{name.rstrip()}'")
print(f"Name after strip(): '{name.strip()}'")